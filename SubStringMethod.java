package StringTsp;

public class SubStringMethod {
    public static void main(String[] args) {
        String s = "This is new java course";
        System.out.println(s);

        s = s.substring(7);
        System.out.println(s);

        System.out.println(s.substring(3));
        System.out.println(s.substring(3,8));
    }
}
