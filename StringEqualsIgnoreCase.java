package StringTsp;

public class StringEqualsIgnoreCase {
    public static void main(String[] args) {
        String s1 = "FaHima";
        String s2 = "Fahima";
        System.out.println(s1.equalsIgnoreCase(s2));

        String s3 = "Jahangir";
        String s4 = "JAHANGIRF";
        System.out.println(s3.equalsIgnoreCase(s4));
    }
}
