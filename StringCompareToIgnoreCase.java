package StringTsp;

public class StringCompareToIgnoreCase {
    public static void main(String[] args) {
        String s1 = "Fahima";
        String s2 = "fahima";
        System.out.println(s1.compareToIgnoreCase(s2));

        String s3 = "Jahangir";
        String s4 = "faiyaz";
        System.out.println(s3.compareToIgnoreCase(s4));
    }
}
