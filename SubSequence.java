package StringTsp;

public class SubSequence {
    public static void main(String[] args) {
        String s = new String();
        s = "This is java course";

        System.out.println(s.subSequence(3, 8));
        System.out.println(s.subSequence(2, 13));
        System.out.println(s.subSequence(2, 17));
    }
}
