package StringTsp;

public class StringBasic {
    public static void main(String[] args) {
        char [] c = {'J', 'A', 'H', 'A', 'N'};
        String s = new String(c);
        System.out.println(s);

        String s1 = new String();
        s1 = "Jhangir";
        System.out.println(s1);

        String s2 = "Alom";
        System.out.println(s2);
    }
}
